using System;

namespace VariablesExample
{
    class Program
    {
        static void Main(string[] args)
        {
            // Declaración de variables
            int numeroEntero = 10;
            double numeroDecimal = 20.5;
            char caracter = 'A';
            string cadenaTexto = "Hola, mundo!";
            bool esVerdadero = true;

            // Imprimir variables
            Console.WriteLine("Número entero: " + numeroEntero);
            Console.WriteLine("Número decimal: " + numeroDecimal);
            Console.WriteLine("Carácter: " + caracter);
            Console.WriteLine("Cadena de texto: " + cadenaTexto);
            Console.WriteLine("Booleano: " + esVerdadero);
        }
    }
}